     public void GetDetails(){
         
        try{
            String id =txtmemid.getText();
            String is = txtisbn.getText();
            
            String sql="SELECT * FROM `issue` WHERE memberid= '"+id+"' && isbn = '"+is+"'" ;
                   
           pst = conn.prepareStatement(sql);
           rs=pst.executeQuery();
              
            if(rs.next()){
            txtmemname.setText(rs.getString("membername"));
            txtbookname.setText(rs.getString("bookname"));
            txtissuedate.setText(rs.getString("issuedate"));
            txtduedate.setText(rs.getString("duedate"));
            txtissueid.setText(rs.getString("issueid"));
            
            rs.close();
            pst.close();
        }
            else{
            JOptionPane.showMessageDialog(null, "Incorrect Details");
        }
       
       
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       
       }
    }
     
