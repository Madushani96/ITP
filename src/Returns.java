    private void loadTable(){
    
        try{
        
            String sql ="SELECT `returnid`, `issueid`, `memberid`, `membername`, `isbn`, `bookname`, `issuedate`, `duedate` , `returndate` , `fine` FROM `return`";
            pst = conn.prepareStatement(sql);
            rs=pst.executeQuery();
            tblreturns.setModel(net.proteanit.sql.DbUtils.resultSetToTableModel(rs));
            
            rs.close();
            pst.close();
            
        }catch(Exception e){
            
            JOptionPane.showMessageDialog(rootPane, e);
        
        }
    }  
  
    
