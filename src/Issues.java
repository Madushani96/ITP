     private void SearchMember(){
         
        try{
            String id =txtmemid.getText();
            String sql="SELECT * FROM `member` WHERE memberid= '"+id+"' && status = 'eligible'";
                   
           pst = conn.prepareStatement(sql);
           rs=pst.executeQuery();
              
            if(rs.next()){
            txtmemname.setText(rs.getString("name"));
           
        }
            else{
            JOptionPane.showMessageDialog(null, "Member Uneligible");
        }
       
       
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       
       }
    }
     
      private void SearchBook(){
         
        try{
            String id =txtisbn.getText();
            
            
           String sql="SELECT * FROM `book` WHERE isbn= '"+id+"' && type ='borrow' && status = 'available'";            
           pst = conn.prepareStatement(sql);
           rs=pst.executeQuery();
              
            if(rs.next()){
            txtbookname.setText(rs.getString("name"));
           
        }
            else{
            JOptionPane.showMessageDialog(null, "Book Unavailable");
        }
       
       
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       
       }
    }
      
      
